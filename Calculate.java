public class Calculate
{
    public static int intSum(int number1, int number2)
    {
        int result = number1 + number2;
        return result;
    }

    public static int intSub(int number1, int number2)
    {
        int result = number1 - number2;
        return result;
    }

    public static double intDiv(double number1, double number2)
    {
        double result = number1 / number2;
        return result;
    }

    public static int intMulti(int number1, int number2)
    {
        int result = number1 * number2;
        return result;
    }
}